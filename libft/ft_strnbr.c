#include "libft.h"

int		ft_strnbr(char *str)
{
	int		i;

	i = 0;
	while (str[i] >= 48 && str[i] <= 57 && str[i])
		i++;
	if (str[i] == '\0')
		return (1);
	else
		return (0);
}
